document.addEventListener("DOMContentLoaded", () => {
  // Khởi tạo ngôn ngữ từ LocalStorage, mặc định là tiếng Việt
  let currentLang = localStorage.getItem("user_lang") || "vi";
  const langToggleBtn = document.getElementById("lang-toggle");
  const langText = document.getElementById("lang-text");

  // Hàm áp dụng ngôn ngữ
  const applyLanguage = (lang) => {
    // Đổi text trên nút
    if (langText) langText.innerText = lang === "vi" ? "VIE" : "ENG";

    // Quét toàn bộ DOM tìm data-i18n
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (translations[lang][key]) {
        el.innerHTML = translations[lang][key]; // Dùng innerHTML để render các thẻ HTML bên trong chuỗi dịch
      }
    });

    // Đổi thuộc tính lang của thẻ html để tốt cho SEO
    document.documentElement.lang = lang === "vi" ? "vi" : "en";
  };

  // Lắng nghe sự kiện đổi ngôn ngữ
  if (langToggleBtn) {
    langToggleBtn.addEventListener("click", () => {
      currentLang = currentLang === "vi" ? "en" : "vi";
      localStorage.setItem("user_lang", currentLang);
      applyLanguage(currentLang);
    });
  }

  // Chạy lần đầu
  applyLanguage(currentLang);
});
